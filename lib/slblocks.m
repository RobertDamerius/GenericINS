function blkStruct = slblocks
    % This function specifies that the library should appear
    % in the Library Browser and should be cached in the browser repository.

    % Name of the library (filename)
    Browser.Library = 'GenericINSLibrary';

    % Library name that appears in the Library Browser
    Browser.Name = 'Generic INS';

    blkStruct.Browser = Browser;
end

